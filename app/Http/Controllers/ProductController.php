    public function getProduct(Request $request)
    {
        $term = $request->q;

        $products = Product::where('name', 'LIKE', '%' . $term . '%')
            ->select('id', 'name', 'code')
            ->orderBy('id', 'desc')
            ->take(20)
            ->get();

        return response()->json($products, 200);
